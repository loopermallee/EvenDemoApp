import 'dart:async';
import 'package:flutter/services.dart';
import 'package:get/get.dart';
import 'evenai.dart';

class BLEEvents {
  static const EventChannel _eventChannel =
      EventChannel("eventBleReceive"); // ‚úÖ must match Kotlin event channel

  static StreamSubscription? _subscription;

  static void startListening() {
    if (_subscription != null) return;

    _subscription = _eventChannel.receiveBroadcastStream().listen((event) {
      if (event is Map) {
        final type = event["event"];
        final transcript = event["transcript"] ?? "";

        switch (type) {
          case "mic_start":
            print("üé§ Mic START (from Kotlin)");
            EvenAI.get.startListening(Uint8List(0)); // just signal start
            break;
          case "mic_stop":
            print("üõë Mic STOP (from Kotlin)");
            EvenAI.get.processTranscript(transcript);
            break;
          default:
            print("‚ö†Ô∏è Unknown BLE event: $event");
        }
      }
    }, onError: (err) {
      print("‚ö†Ô∏è BLE EventChannel error: $err");
    });
  }

  static void stopListening() {
    _subscription?.cancel();
    _subscription = null;
  }
}